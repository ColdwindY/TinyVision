static int sensor_s_hflip(struct v4l2_subdev *sd, int enable)
{
	data_type get_value;
	data_type set_value;

	if (!(enable == 0 || enable == 1))
		return -1;

	sensor_read(sd, 0x3821, &get_value);
	sensor_dbg("sensor_read(sd, 0x3821, 0x%02x) before hflip\n", get_value);
	if (enable)
		set_value = get_value & 0xfb;
	else
		set_value = get_value | 0x04;
	sensor_write(sd, 0x3821, set_value);

	sensor_read(sd, 0x3821, &get_value);
	sensor_dbg("sensor_read(sd, 0x3821, 0x%02x) after hflip\n", get_value);
	return 0;

}

