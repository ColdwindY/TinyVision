static int os04a10_sensor_vts;

static int sensor_s_exp(struct v4l2_subdev *sd, unsigned int exp_val)
{
	int shutter, frame_length;
	int exp_val_tmp;
	struct sensor_info *info = to_state(sd);

	shutter = exp_val >> 4;
	if (shutter > os04a10_sensor_vts -8)
		frame_length = shutter + 8;
	else
		frame_length = os04a10_sensor_vts;

	sensor_write(sd, 0x380e, (frame_length >> 8) & 0xff);
	sensor_write(sd, 0x380f, frame_length & 0xff);

	exp_val_tmp = exp_val >> 4;

	sensor_write(sd, 0x3501, (exp_val_tmp >> 8) & 0xFF);
	sensor_write(sd, 0x3502, (exp_val_tmp) & 0xFF);

	sensor_dbg("sensor_s_exp %d\n", exp_val_tmp);

	info->exp = exp_val;
	return 0;
}

static int sensor_g_gain(struct v4l2_subdev *sd, __s32 *value)
{
	struct sensor_info *info = to_state(sd);
	*value = info->gain;
	sensor_dbg("sensor_get_gain = %d\n", info->gain);
	return 0;
}

static int sensor_s_gain(struct v4l2_subdev *sd, int gain_val)
{
	struct sensor_info *info = to_state(sd);
	int digi_gain = 1024;
	int ana_gain;

	if (gain_val <= (16 * 1))
		gain_val = 16 * 1;

	if (gain_val <= 16 * 248) {
		ana_gain = (gain_val >> 4);
		digi_gain = 0x0400;
	} else {
		ana_gain = 248;
		digi_gain = (gain_val >> 4) * 1024 / 248;
	}

	ana_gain = ana_gain << 4;
	digi_gain = digi_gain << 6;

	sensor_write(sd, 0x3508, (unsigned char)((ana_gain >> 8) & 0x1f));
	sensor_write(sd, 0x3509, (unsigned char)(ana_gain & 0xf0));

	sensor_write(sd, 0x350a, (unsigned char)((digi_gain >> 16) & 0x0f));
	sensor_write(sd, 0x350b, (unsigned char)(digi_gain >> 8) & 0xff);
	sensor_write(sd, 0x350b, (unsigned char)(digi_gain & 0xc0));

	sensor_dbg("sensor_s_gain %d\n", gain_val);

	info->gain = gain_val;
	return 0;
}

static int sensor_s_exp_gain(struct v4l2_subdev *sd,
				struct sensor_exp_gain *exp_gain)
{
	int exp_val, gain_val;
	struct sensor_info *info = to_state(sd);

	exp_val = exp_gain->exp_val;
	gain_val = exp_gain->gain_val;

	sensor_s_exp(sd, exp_val);
	sensor_s_gain(sd, gain_val);

	info->exp = exp_val;
	info->gain = gain_val;
	return 0;
}

static int sensor_s_sw_stby(struct v4l2_subdev *sd, int on_off)
{
	int ret;
	data_type rdval;

	ret = sensor_read(sd, 0x0100, &rdval);
	if (ret != 0)
		return ret;

	if (on_off == STBY_ON)
		ret = sensor_write(sd, 0x0100, rdval&0xfe);
	else
		ret = sensor_write(sd, 0x0100, rdval|0x01);
	return ret;
}

